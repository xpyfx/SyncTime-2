/** 
 * 取得特定國家的城市列表
 * 優先從手動維護的 COMMON_CITIES 獲取中文城市，
 * 若無資料則從 country-state-city 套件獲取英文城市
 */
export function getCitiesByCountry(countryName: string): string[] {
  // 1. 優先查詢手動維護的中文城市列表
  if (COMMON_CITIES[countryName]) {
    return COMMON_CITIES[countryName];
  }

  // 2. 備援：查詢套件資料
  const country = Country.getAllCountries().find(c => c.name === countryName);
  if (!country) return [];
  
  return City.getCitiesOfCountry(country.isoCode)?.map(city => city.name) || [];
}

/** 獲取所有城市名稱列表 (合併手動與套件) */
export const ALL_CITIES: string[] = Array.from(new Set([
  ...Object.values(COMMON_CITIES).flat(),
  ...City.getAllCities().map(c => c.name)
]));

/** 根據城市名稱反查所屬國家 */
export function getCountryByCity(cityName: string): string | undefined {
  // 1. 手動資料庫查詢
  for (const [country, cities] of Object.entries(COMMON_CITIES)) {
    if (cities.includes(cityName)) return country;
  }

  // 2. 套件查詢
  const city = City.getAllCities().find(c => c.name === cityName);
  if (!city) return undefined;
  const country = Country.getCountryByCode(city.countryCode);
  return country?.name;
}

/** 模糊搜尋城市（不分大小寫） */
export function searchCities(keyword: string): string[] {
  const lower = keyword.toLowerCase();
  
  // 優化：先從手動資料庫找，再從套件找
  const manualMatches = ALL_CITIES.filter(c => c.toLowerCase().includes(lower));
  if (manualMatches.length > 0) return manualMatches.slice(0, 50);

  return City.getAllCities()
    .filter((c) => c.name.toLowerCase().includes(lower))
    .slice(0, 50)
    .map(c => c.name);
}

/** 模糊搜尋國家 */
export function searchCountries(keyword: string): string[] {
  const lower = keyword.toLowerCase();
  return COUNTRIES.filter((c) => c.toLowerCase().includes(lower)).slice(0, 50);
}

